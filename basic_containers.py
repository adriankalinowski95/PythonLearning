
def create_array():
    """Create and return an array."""
    return [1, 2, 3, 4, 5]

def create_map():
    """Create and return a map."""
    return {'a': 1, 'b': 2, 'c': 3}

def create_set():
    """Create and return a set."""
    return {1, 2, 3, 4, 5}

def create_tuple():
    """Create and return a tuple."""
    return (1, 2, 3, 4, 5)

def create_string():
    """Create and return a string."""
    return "Hello, World!"

def create_bytearray():
    """Create and return a bytearray."""
    return bytearray(b'Hello, World!')

